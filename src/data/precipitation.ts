export const getTopRainfallDistricts = (count: number = 10): PrecipitationData[] => {
  return [...precipitationData]
    .sort((a, b) => b.precipAnomalyPct - a.precipAnomalyPct)
    .slice(0, count);
};

export const getFloodRiskDistricts = (count: number = 10): PrecipitationData[] => {
  // Flood risk based on high precipitation anomaly
  return getTopRainfallDistricts(count);
};
