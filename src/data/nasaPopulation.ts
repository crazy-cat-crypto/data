// Helper function to get top dense districts
export const getTopDenseDistricts = (count: number = 10): NASAPopulationData[] => {
  return [...nasaPopulationData]
    .sort((a, b) => b.densityPerSqKm - a.densityPerSqKm)
    .slice(0, count);
};

// Helper function to get top populous districts
export const getTopPopulousDistricts = (count: number = 10): NASAPopulationData[] => {
  return [...nasaPopulationData]
    .sort((a, b) => b.population - a.population)
    .slice(0, count);
};

// Calculate national average density
export const getNationalAvgDensity = (): number => {
  const total = nasaPopulationData.reduce((sum, d) => sum + d.densityPerSqKm, 0);
  return total / nasaPopulationData.length;
};
