// Helper functions
export const getTopVegetatedDistricts = (count: number = 10): NASAVegetationData[] => {
  return [...nasaVegetationData]
    .sort((a, b) => b.ndvi - a.ndvi)
    .slice(0, count);
};

export const getNationalAvgNDVI = (): number => {
  const total = nasaVegetationData.reduce((sum, d) => sum + d.ndvi, 0);
  return total / nasaVegetationData.length;
};

export const getNationalAvgEVI = (): number => {
  const total = nasaVegetationData.reduce((sum, d) => sum + d.evi, 0);
  return total / nasaVegetationData.length;
};
